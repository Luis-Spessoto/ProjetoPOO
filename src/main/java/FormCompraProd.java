    public void comprarProdInserirLista(){
        p = new Produto();
        try{
            p.setNome(cxNomeProd.getText());
        }
        catch(ProdMaxTamException pmt){
            pmt.impErrorExc();
            p = pmt.corrigirTamProd(p);
        }
        try{          
            p.setPreco(Integer.parseInt(cxValorUnit.getText()));
        }
        catch(PrecoNegException pre){
            pre.impErrorExc();
            p = pre.corrigirValorNeg(p);
        }
        try{
            p.setCodProduto(Integer.parseInt(cxCodProd.getText()));
            p.setQuantidade(Integer.parseInt(cxQuantProd.getText()));
            p.getFornecedor().setId(Integer.parseInt(cxIdForn.getText()));
            p.getFuncionario().setId(Integer.parseInt(cxIdFuncio.getText()));
        
            if(gerEst.consProdLista(p) == null){
                JOptionPane.showMessageDialog(
                        null,
                        "Produto cadastrado com sucesso!",
                        "Compra de produtos",
                        JOptionPane.INFORMATION_MESSAGE
                );
                limpar();
            }
            else{
                int resposta = JOptionPane.showConfirmDialog(
                null,
                "O produto com esse cÃ³digo jÃ¡ existe no estoque. Deseja atualizar a quantidade?",
                "Atualizar Produto",
                JOptionPane.YES_NO_OPTION
                );
                if (resposta == JOptionPane.YES_OPTION) {
                    gerEst.consProdLista(p).setQuantidade(gerEst.consProdLista(p).getQuantidade() + p.getQuantidade());
                    JOptionPane.showMessageDialog(
                        null,
                        "Quantidade do produto atualizada com sucesso!\nQuantidade atual: " + gerEst.consProdLista(p).getQuantidade(),
                        "AtualizaÃ§Ã£o ConcluÃ­da",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                } else {
                    JOptionPane.showMessageDialog(
                        null,
                        "A quantidade do produto nÃ£o foi alterada.",
                        "OperaÃ§Ã£o Cancelada",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                }
                limpar();
            }
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(
                null,
                "O valor unitario deve ser um inteiro!",
                "ERROR tipo de dados",
                JOptionPane.ERROR_MESSAGE
            );
            cxValorUnit.setText("");
            cxValorUnit.requestFocus();
        }
        p = gerEst.comprarProd(p);
    }
